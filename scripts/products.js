// Función para crear el elemento multimedia (imagen o video)
function createMediaElement(product) {
  if (product.type === 'video') {
    const video = document.createElement('video');
    video.autoplay = true;
    video.loop = true;
    video.muted = true;
    video.style.maxWidth = '100%';
    video.style.height = 'auto';

    const source = document.createElement('source');
    source.src = product.src;
    source.type = 'video/mp4';

    video.appendChild(source);
    return video;
  } else {
    const img = document.createElement('img');
    img.src = product.src;
    img.alt = product.title;
    return img;
  }
}

// Función para renderizar un producto
function createProductCard(product) {
  const productCard = document.createElement('div');
  productCard.className = 'product-card';

  const productImage = document.createElement('div');
  productImage.className = 'product-image';

  const mediaElement = createMediaElement(product);
  productImage.appendChild(mediaElement);

  const quickAddBtn = document.createElement('button');
  quickAddBtn.className = 'quick-add-btn';

  const sizeOptions = document.createElement('div');
  sizeOptions.className = 'size-options';

  product.sizes.forEach((size) => {
    const span = document.createElement('span');
    span.textContent = size;
    sizeOptions.appendChild(span);
  });

  quickAddBtn.appendChild(sizeOptions);
  productImage.appendChild(quickAddBtn);

  const productInfo = document.createElement('div');
  productInfo.className = 'product-info';

  const productInfoTop = document.createElement('div');
  productInfoTop.className = 'product-info-top';

  const title = document.createElement('h3');
  title.textContent = product.title;

  const wishlistBtn = document.createElement('button');
  wishlistBtn.className = 'wishlist-btn';
  wishlistBtn.setAttribute('aria-label', 'Add to wishlist');
  wishlistBtn.innerHTML = `
      <svg width="22" height="22" viewBox="0 0 22 22" fill="none" stroke-width="1.2">
        <path d="M18 18L11.5 13L5 18V3H11.5H18V18Z" stroke="currentColor" stroke-linejoin="round"></path>
      </svg>
    `;

  productInfoTop.appendChild(title);
  productInfoTop.appendChild(wishlistBtn);

  const price = document.createElement('p');
  price.className = 'price';
  price.textContent = `S/. ${product.price.toFixed(2)}`;

  productInfo.appendChild(productInfoTop);
  productInfo.appendChild(price);

  productCard.appendChild(productImage);
  productCard.appendChild(productInfo);

  return productCard;
}

function renderProductGrid() {
  const productGrid = document.querySelector('.product-grid');
  if (!productGrid) return;

  productGrid.innerHTML = '';

  products.forEach((product) => {
    const productCard = createProductCard(product);
    productGrid.appendChild(productCard);
  });
}

document.addEventListener('DOMContentLoaded', renderProductGrid);
